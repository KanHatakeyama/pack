def llm_gen(llm,prompt_list,temperature=0.7):

    outputs = llm.generate(
        prompt_list,
        sampling_params=SamplingParams(
            temperature=temperature,
            max_tokens=1024,
            repetition_penalty=1.2,
            top_k=50,
        )
    )
    return [i.outputs[0].text.strip() for i in outputs]


