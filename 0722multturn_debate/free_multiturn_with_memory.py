def filter_record_list(record_list,final_check=False):
    filtered=[]
    for record in record_list:
        reject_flag=False
        for key in record:
            if key=="id":
                continue
            if record[key]=="":
                reject_flag=True
                break
            if final_check:
                if record[key] is None:
                    reject_flag=True
                elif len(record[key])<10:
                    reject_flag=True
        if reject_flag:
            continue
        filtered.append(record)
    return filtered



