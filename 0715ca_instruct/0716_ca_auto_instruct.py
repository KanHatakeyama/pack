def set_random_seed():
    # 現在時刻を取得
    now = datetime.now()
    # 現在時刻をシード値として変換
    seed = now.year + now.month + now.day + now.hour + now.minute + now.second + now.microsecond
    # 乱数を生成してシード値に追加
    seed += random.randint(0, 1000000)
    for i in range(1000):
        seed+=1
    # ランダムシードを設定
    print("setting random seed: ",seed)
    random.seed(seed)
    return seed

